namespace AcornDB.Query
{
    /// <summary>
    /// Comparison operators that can be used with indexes
    /// </summary>
    public enum ComparisonOperator
    {
        Equal,              // ==
        NotEqual,           // !=
        GreaterThan,        // >
        GreaterThanOrEqual, // >=
        LessThan,           // <
        LessThanOrEqual     // <=
    }
}
